#include <unistd.h>

char	*ft_strupcase(char *str)
{
	int	i;
	int	n;

	i = 0;
	n = 'a' - 'A';
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] -= n;
		i++;
	}
	return (str);
}
