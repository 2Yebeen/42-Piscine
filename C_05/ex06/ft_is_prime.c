int	ft_is_prime(int nb)
{
	int	i;
	int	num;

	if (nb <= 1)
		return (0);
	num = nb / 2;
	i = 2;
	while (i <= num)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
