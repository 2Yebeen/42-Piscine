#include <unistd.h>

int	main(int ac, char **av)
{
	int	j;

	j = 0;
	if (ac > 0)
	{
		while (av[0][j])
			j++;
		write(1, av[0], j);
		write(1, "\n", 1);
	}
	return (0);
}
