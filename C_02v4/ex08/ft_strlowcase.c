#include <unistd.h>
char	*ft_strlowcase(char *str)
{
	int	i;
	int	n;

	i = 0;
	n = 'a' - 'A';
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += n;
		i++;
	}
	return (str);
}
